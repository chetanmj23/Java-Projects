    private boolean validData() 
    {
        if(productNamejTextField.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Product Name..");
            return false;
        }
        if(ceilingjTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter ceiling Price..");
            return false;
        }
        if(floorJTextField2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Floor Price..");
            return false;
        }
        if(qtyTextField3.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Quantity..");
            return false;
        }
        if(marketCeilingTextField5.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Market Ceiling price..");
            return false;
        }
        if(marketFloorTextField6.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Market floor price..");
            return false;
        }
        return true;
    }
        

