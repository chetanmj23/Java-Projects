    private void addProductJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductJButton1ActionPerformed
        // TODO add your handling code here:
        if (validData())
        {
            Product p = supplier.getProductCatalog().addProduct();
            p.setProductName(productNamejTextField.getText());
            p.setCeilingPrice(Integer.parseInt(ceilingjTextField1.getText()));
            p.setFloorPrice(Integer.parseInt(floorJTextField2.getText()));
            p.setQuantity(Integer.parseInt(qtyTextField3.getText()));
            p.getMarketOffer().setCeilingPrice(Integer.parseInt(marketCeilingTextField5.getText()));
            p.getMarketOffer().setFloorPrice(Integer.parseInt(marketFloorTextField6.getText()));
            p.getMarketOffer().getMarket().setMarketName((String) marketjComboBox2.getSelectedItem());

            JOptionPane.showMessageDialog(null, "Product added!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        

    }//GEN-LAST:event_addProductJButton1ActionPerformed

