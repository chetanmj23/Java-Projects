    private void salesPersonLoginjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salesPersonLoginjButtonActionPerformed
        // TODO add your handling code here:
            String name = userNameTextField.getText();
            String pwd = JPasswordField.getText();
            String type = "salesman";
            if ((name.equalsIgnoreCase("")) || pwd.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Please enter your credentials");
                return;
            }
        for(Person p : business.getEmployeeDirectory().getPersonList()) {
            
            if(p.getPersonName().equals(name) && p.getPassword().equals(pwd))
            {
                if(p.getPersonType().equals(type))
                {
                ManageCustomersJPanel panel = new ManageCustomersJPanel(userProcessContainer,business,p);
                userProcessContainer.add("ManageCustomersJPanel", panel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Please enter valid credentials");
                    reset();
                }
            }
           
        }
        
    }//GEN-LAST:event_salesPersonLoginjButtonActionPerformed

    private void customerLoginjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerLoginjButtonActionPerformed
        // TODO add your handling code here:
        String username = userNameTextField.getText();
        String pwd = JPasswordField.getText();
        boolean flag = false;
        if ((username.equalsIgnoreCase("")) || pwd.equals(""))
            {
            JOptionPane.showMessageDialog(null, "Please enter your credentials");
            return;
            }
        
        for(Customer customer: business.getCustomerDiretory().getCustomerList())
        {
            if(customer.getCustomerName().equals(username) && customer.getCustomerPwd().equals(pwd))
            {
                flag = true;
                CustomerOrderJPanel panel = new CustomerOrderJPanel(userProcessContainer,business,business.getSupplierDirectory().getSupplierList(),customer);
                userProcessContainer.add("AdminLoginJPanel", panel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
        if(!flag)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid credentials");
            reset();
        }
        
    }//GEN-LAST:event_customerLoginjButtonActionPerformed

