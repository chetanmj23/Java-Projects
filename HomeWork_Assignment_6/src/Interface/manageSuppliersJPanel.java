    public void refreshTable()
    {
        
        DefaultTableModel model = (DefaultTableModel) supplierjTable.getModel();
        model.setRowCount(0);
        for (Supplier s : supplierList) {
            Object row[] = new Object[1];
            row[0] = s;
            model.addRow(row);
        }
    }
  

