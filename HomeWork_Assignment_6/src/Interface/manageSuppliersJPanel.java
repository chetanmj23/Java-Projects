    private void viewSupplierjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSupplierjButton1ActionPerformed
        // TODO add your handling code here:
        
        int row = supplierjTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else
        {
            Supplier s = (Supplier)supplierjTable.getValueAt(row,0);
            ViewSupplierJPanel panel = new ViewSupplierJPanel(userProcessContainer,s);
            userProcessContainer.add("ViewSupplierJPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);            
        }
        
        
         
    }//GEN-LAST:event_viewSupplierjButton1ActionPerformed

    private void removeSupplierjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeSupplierjButton2ActionPerformed
        // TODO add your handling code here:
        int row = supplierjTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Supplier s = (Supplier) supplierjTable.getValueAt(row, 0);
        business.getSupplierDirectory().removeSupplier(s);
        JOptionPane.showMessageDialog(null, "Supplier Data Removed");
        refreshTable();
        
    }//GEN-LAST:event_removeSupplierjButton2ActionPerformed

