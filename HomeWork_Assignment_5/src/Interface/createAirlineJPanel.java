    private void addAirlinerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAirlinerButtonActionPerformed
        // TODO add your handling code here:
        String airlinename = airlinerNameCreatetxtfld.getText();
        if(airlinename.equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter a Airline Name");
        }
        else
        {
            String airlineName = airlinerNameCreatetxtfld.getText();
            
            Airline airline = new Airline();
            airline.setAirlineName(airlineName);
            
            travelagency.getAirlinerDirectory().add(airline);
            
            JOptionPane.showMessageDialog(null, "Airline added Successfully");
            
            airlinerNameCreatetxtfld.setText("");
            
        }
    }//GEN-LAST:event_addAirlinerButtonActionPerformed


