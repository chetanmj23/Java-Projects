    private void searchBestDealsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBestDealsBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)bestDealsJTable.getModel();
        dtm.setRowCount(0);
        
        for(Airline airline : travelagency.getAirlinerDirectory())
        {
            for(Flight flight : airline.getFlight())
            {
                if(flight.getSource().equalsIgnoreCase(fromFlighttxtfld.getText()) && flight.getDestination().equalsIgnoreCase(toFlighttxtfld2.getText()) && flight.getTimeoftheday().equalsIgnoreCase(timeFlighttxtfld1.getText()))
                {
                    Object row[] = new Object [6];
                        row[0] = airline;
                        row[1] = flight.getFlightId();
                        row[2]=flight.getSource();
                        row[3] = flight.getDestination();
                        row[4]=flight.getCapacity();
                        row[5]=flight.getTimeoftheday();
                
                
                        dtm.addRow(row);
                }
                    
                }
                
            }
        
        
        
    }//GEN-LAST:event_searchBestDealsBtnActionPerformed

