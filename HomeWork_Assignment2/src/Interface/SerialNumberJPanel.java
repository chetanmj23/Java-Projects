    private void serialNumberSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serialNumberSearchButtonActionPerformed
        // TODO add your handling code here:
         DefaultTableModel dtm = (DefaultTableModel)serialNumberTable.getModel();
        dtm.setRowCount(0);
        for(Airplane airplane : fleet.getFleet()){
            if(airplane.getSerialNumber().equalsIgnoreCase(enterSerialNumberTextField.getText())){
                Object row[] = new Object[3];
                row[0]= airplane;
                row[1]=airplane.getManufacturer();
                row[2]=airplane.getDepartureTime();
                
                dtm.addRow(row);
                
            }
        }
        
    }//GEN-LAST:event_serialNumberSearchButtonActionPerformed

    private void serialViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serialViewButtonActionPerformed
        // TODO add your handling code here:
        
         int selectedrow = serialNumberTable.getSelectedRow();
        
        if (selectedrow >= 0)
        {
            Airplane A = (Airplane)serialNumberTable.getValueAt(selectedrow, 0);
            serialNoTextFld.setText(A.getSerialNumber());
            modelNoTextFld.setText(A.getModelNumber());
            departureTimeTextFld.setText(A.getDepartureTime());
            manufacturedByTextFld.setText(A.getManufacturer());
            manufacturedYearTextFld.setText(A.getManufacturedYear());
            airplaneCapacityTextFld.setText(A.getCapacity());
            certificateTextFld.setText(A.getValidity());
            
        }
        else
            JOptionPane.showMessageDialog(null,"please select any row to proceed");
           
    }//GEN-LAST:event_serialViewButtonActionPerformed


