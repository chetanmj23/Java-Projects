    private boolean validData() {
        //To change body of generated methods, choose Tools | Templates.
        if(serialNoTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Serial Number..");
            return false;
        }
         if(modelNoTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Model Number..");
            return false;
        }
         if(departureTimeTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Departure Time..");
            return false;
        }
         if(manufacturedByTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Manufacturer name..");
            return false;
        }
         if(manufacturedYearTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Manufactured Year..");
            return false;
        }
         if(airplaneCapacityTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Seating Capacity of Airplane..");
            return false;
        }
         if(certificateTextFld.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter the Certificate Valid till Year..");
            return false;
        }
        
         return true;
         
    }
