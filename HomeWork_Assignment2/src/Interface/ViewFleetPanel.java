    private void savejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savejButtonActionPerformed
        // TODO add your handling code here:
    if (validData()){
        int selectedrow = viewFleetTable.getSelectedRow();
        Airplane A = (Airplane)viewFleetTable.getValueAt(selectedrow, 0);
        String modelnumber = modelNoTextFld.getText();
        String serialnumber = serialNoTextFld.getText();
        String departuretime = departureTimeTextFld.getText();
        String manufacturer = manufacturedByTextFld.getText();
        String manufacturedyear = manufacturedYearTextFld.getText();
        String capacity = airplaneCapacityTextFld.getText();
        String certificate = certificateTextFld.getText();
        //Airplane A = fleet.addPlanes();
        A.setSerialNumber(serialnumber);
        A.setModelNumber(modelnumber);
        A.setDepartureTime(departuretime);
        A.setManufacturer(manufacturer);
        A.setManufacturedYear(manufacturedyear);
        A.setCapacity(capacity);
        A.setValidity(certificate); 
       
        
        JOptionPane.showMessageDialog(null, "Fleet successfully Updated!");
        displaytable();
    }
    }//GEN-LAST:event_savejButtonActionPerformed


