    private void displaytable() {
        DefaultTableModel dtm = (DefaultTableModel)viewFleetTable.getModel();
        dtm.setRowCount(0);
        for(Airplane vs : fleet.getFleet())
        {
            
            Object row[]= new Object[8];
            row [0] = vs;
            row [1] = vs.getModelNumber();
            row [2] = vs.getDepartureTime();
            row [3] = vs.getManufacturer();
            row [4] = vs.getManufacturedYear();
            row [5] = vs.getCapacity();
            row [6] = vs.getValidity();
            
            dtm.addRow(row);
        }
    }

