    private void viewDetailsBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBTNActionPerformed
        // TODO add your handling code here:
        int selectedrow = boeingTable.getSelectedRow();
        
        if (selectedrow >= 0)
        {
            Airplane A = (Airplane)boeingTable.getValueAt(selectedrow, 0);
            serialNoTextFld.setText(A.getSerialNumber());
            modelNoTextFld.setText(A.getModelNumber());
            departureTimeTextFld.setText(A.getDepartureTime());
            manufacturedByTextFld.setText(A.getManufacturer());
            manufacturedYearTextFld.setText(A.getManufacturedYear());
            airplaneCapacityTextFld.setText(A.getCapacity());
            certificateTextFld.setText(A.getValidity());
            
        }
        else
            JOptionPane.showMessageDialog(null,"please select any row to proceed");
           
    }//GEN-LAST:event_viewDetailsBTNActionPerformed

    private void boeingjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boeingjButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)boeingTable.getModel();
        dtm.setRowCount(0);
        for(Airplane airplane : fleet.getFleet()){
            if(airplane.getManufacturer().equalsIgnoreCase(BoeingjTextField.getText())){
                Object row[] = new Object[3];
                row[0]= airplane;
                row[1]=airplane.getManufacturer();
                row[2]=airplane.getCapacity();
                dtm.addRow(row);
                
            }
        }
    }//GEN-LAST:event_boeingjButtonActionPerformed


