    private void expiredplanes()
    {
        DefaultTableModel dtm = (DefaultTableModel)expiredCertificateTable.getModel();
        dtm.setRowCount(0);
        
        for(Airplane airplane : fleet.getFleet())
        {
            if (Integer.parseInt(airplane.getValidity()) < 2017) {
                Object row[] = new Object[3];
                row[0] = airplane;
                row[1] = airplane.getManufacturer();
                row[2] = airplane.getValidity();
                dtm.addRow(row);
            }
            
        }
        
    }

