    private void updatejButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButton2ActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = personjTable.getSelectedRow();
        if (selectedrow >= 0)
        {
            Person person = (Person)personjTable.getValueAt(selectedrow, 0);
            UpdatePersonJPanel panel = new UpdatePersonJPanel(UserProcessContainer, person);
            UserProcessContainer.add("UpdatePersonJPanel", panel);
            CardLayout layout = (CardLayout) UserProcessContainer.getLayout();
            layout.next(UserProcessContainer);
        }else
        {
            JOptionPane.showMessageDialog(null,"Please select row to proceed");
        }
    }//GEN-LAST:event_updatejButton2ActionPerformed

