    public void populatetable() {
        DefaultTableModel dtm = (DefaultTableModel) personjTable.getModel();
        dtm.setRowCount(0);

        for (Person person : business.getPersondirectory().getPersonDirectory()) {
            Object row[] = new Object[4];
            row[0] = person;
            row[1] = person.getAge();
            row[2] = person.getsSNumber();
            dtm.addRow(row);
        }
    }

