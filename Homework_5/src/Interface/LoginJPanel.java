    private void loginjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginjButtonActionPerformed
        // TODO add your handling code here:
        String userid = userIDjTextField1.getText();
        String pwd = loginjPasswordField.getText();

        if ((userid.equalsIgnoreCase("")) || pwd.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter your credentials");
            return;
        }
            UserAccount Ua = business.getUseraccountdirectory().ValidUser(userid, pwd);
            if (Ua == null) 
            {
                JOptionPane.showMessageDialog(null, "Wrong credentials entered");
                userIDjTextField1.setText("");
                loginjPasswordField.setText("");
            }
           
//           UserAccount ua1 = business.getUseraccountdirectory().ValidStatus("Active");
//           if(ua1 == null)
//                   {
//                       JOptionPane.showMessageDialog(null, "Account is Disabled!  CONTACT YOUR SYSTEM ADMIN");
//                   }
            
        for (Person person : business.getPersondirectory().getPersonDirectory())
        {
            try {
            if (person.getUseraccount().getUserID().equalsIgnoreCase(userIDjTextField1.getText()) && person.getUseraccount().getPassword().equals(loginjPasswordField.getText())) 
            {
                if(person.getUseraccount().getStatus().equalsIgnoreCase("Inactive"))
                {
                    JOptionPane.showMessageDialog(null, "Account is Disabled!  CONTACT YOUR SYSTEM ADMIN");
                    reset();
                }
                else
                {
                    
                    if (person.getUseraccount().getRole().equalsIgnoreCase("System Admin")) 
                    {
                    SysAdminWorkAreaPanel panel = new SysAdminWorkAreaPanel(UserProcessContainer,business,person);
                    UserProcessContainer.add("SysAdminWorkAreaPanel", panel);
                    CardLayout layout = (CardLayout) UserProcessContainer.getLayout();
                    layout.next(UserProcessContainer);
                    }
                    else if (person.getUseraccount().getRole().equalsIgnoreCase("HR Admin"))
                    {
                    HRAdminJPanel panel = new HRAdminJPanel(UserProcessContainer,business,person);
                    UserProcessContainer.add("HRAdminJPanel", panel);
                    CardLayout layout = (CardLayout) UserProcessContainer.getLayout();
                    layout.next(UserProcessContainer);
                    }
                    else
                    {
                        UserJPanel panel = new UserJPanel(UserProcessContainer,business,person);
                        UserProcessContainer.add("UserJPanel", panel);
                        CardLayout layout = (CardLayout) UserProcessContainer.getLayout();
                        layout.next(UserProcessContainer); 
                    }
                }
            }
            }catch (NullPointerException e){}

        }
 
    }//GEN-LAST:event_loginjButtonActionPerformed

