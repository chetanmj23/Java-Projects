    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = jTable1.getSelectedRow();
        
        Employee emp = (Employee) jTable1.getValueAt(selectedRow, 0);
        //Enterprise ent = (Enterprise) jTable1.getValueAt(selectedRow, 2);
        
        Organization docOrg = null;
        
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if(o instanceof DoctorOrganization) {
                docOrg = o;
            }
        }
        
        docOrg.getEmployeeDirectory().getEmployeeList().remove(emp);
        JOptionPane.showMessageDialog(null, "Doctor removed");
        
    }//GEN-LAST:event_jButton1ActionPerformed

