    private void populate() {
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        model.setRowCount(0);
        
        for(Feedback f : organization.getFeedbacklist().getFeedbackPool()) {
            Object[] row = new Object[3];
            
            //row[0] = f;
            row[0] = f.getDoctor();
            row[1] = f;
            row[2] = f.getHospital();
            model.addRow(row);
        }
        
        
    }

